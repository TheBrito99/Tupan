import React, { useContext, useEffect, useState } from 'react';
import { Link } from 'react-router-dom';
import { WasmContext } from '../contexts/WasmContext';
import { ThermalEditor } from '@tupan/ui-framework/components/ThermalEditor';
import '../styles/EditorPage.css';

/**
 * Thermal Analysis Page
 * Phase 2A: Heat transfer modeling with resistances, capacitances, and convection
 */
const ThermalPage: React.FC = () => {
  const wasmLoader = useContext(WasmContext);
  const [isReady, setIsReady] = useState(false);
  const [error, setError] = useState<string | null>(null);

  useEffect(() => {
    if (wasmLoader) {
      try {
        setIsReady(true);
        setError(null);
      } catch (err) {
        const errorMsg = err instanceof Error ? err.message : String(err);
        setError(`Failed to initialize thermal system: ${errorMsg}`);
      }
    }
  }, [wasmLoader]);

  if (!isReady) {
    return (
      <div className="editor-page loading">
        <div className="loading-spinner">
          <p>Loading Thermal Analysis...</p>
        </div>
      </div>
    );
  }

  if (error) {
    return (
      <div className="editor-page error">
        <div className="error-box">
          <h2>⚠️ Error</h2>
          <p>{error}</p>
          <button onClick={() => window.location.reload()}>Retry</button>
        </div>
      </div>
    );
  }

  return (
    <div className="editor-page">
      <header className="editor-header">
        <Link to="/" className="back-button">← Back to Dashboard</Link>
        <h1>Thermal Analysis</h1>
        <p>Model heat transfer with thermal resistances, capacitances, and flows</p>
      </header>

      <main className="editor-main">
        <ThermalEditor />
      </main>
    </div>
  );
};

export default ThermalPage;
