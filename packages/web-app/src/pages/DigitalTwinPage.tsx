import React, { useContext, useEffect, useState } from 'react';
import { Link } from 'react-router-dom';
import { WasmContext } from '../contexts/WasmContext';
import { DigitalTwinDashboard } from '@tupan/ui-framework/components/DigitalTwinDashboard';
import '../styles/EditorPage.css';

/**
 * Digital Twin Dashboard Page
 * Phase 3A: Real-time prediction and closed-loop validation of swarm behavior
 */
const DigitalTwinPage: React.FC = () => {
  const wasmLoader = useContext(WasmContext);
  const [isReady, setIsReady] = useState(false);
  const [error, setError] = useState<string | null>(null);

  useEffect(() => {
    if (wasmLoader) {
      try {
        setIsReady(true);
        setError(null);
      } catch (err) {
        const errorMsg = err instanceof Error ? err.message : String(err);
        setError(`Failed to initialize digital twin: ${errorMsg}`);
      }
    }
  }, [wasmLoader]);

  if (!isReady) {
    return (
      <div className="editor-page loading">
        <div className="loading-spinner">
          <p>Loading Digital Twin Dashboard...</p>
        </div>
      </div>
    );
  }

  if (error) {
    return (
      <div className="editor-page error">
        <div className="error-box">
          <h2>⚠️ Error</h2>
          <p>{error}</p>
          <button onClick={() => window.location.reload()}>Retry</button>
        </div>
      </div>
    );
  }

  return (
    <div className="editor-page">
      <header className="editor-header">
        <Link to="/" className="back-button">← Back to Dashboard</Link>
        <h1>Digital Twin Dashboard</h1>
        <p>Real-time prediction and closed-loop validation of swarm behavior</p>
      </header>

      <main className="editor-main">
        <DigitalTwinDashboard />
      </main>
    </div>
  );
};

export default DigitalTwinPage;
